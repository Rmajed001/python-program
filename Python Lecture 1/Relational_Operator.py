# Explanation:
# a == b: Checks if a is equal to b (False).
# a != b: Checks if a is not equal to b (True).
# a < b: Checks if a is less than b (True).
# a > b: Checks if a is greater than b (False).
# a <= b: Checks if a is less than or equal to b (True).
# a >= b: Checks if a is greater than or equal to b (False).
# These operators are used to compare values and return a boolean result (True or False).